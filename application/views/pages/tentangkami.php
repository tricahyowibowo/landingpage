        <span>Mirota</span> merupakan singkatan dari <span>Minuman, Roti dan Tart</span>. Sedangkan <span>KSM</span> sendiri merupakan singkatan dari kata <span>Kinder</span> yang berarti anak-anak, <span>Sugar</span> yang berarti gula (manis), dan <span>Milk</span> yang berarti susu, sehingga bila digabungkan nama <span>KSM</span> itu berarti <span>Susu Manis untuk Anak</span>.
