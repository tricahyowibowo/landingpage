        <br>
        <br>
        Berkat Semangat dan kerja keras beliau, kemudian mencoba melebarkan sayap dengan menciptakan usaha baru di bidang industri susu yang secara resmi berdiri pada tanggal <span>11 Juli 1972</span> dengan nama <span>CV Mirota KSM, Inc</span>. 
        </p>
